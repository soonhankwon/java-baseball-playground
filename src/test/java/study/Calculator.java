package study;

public class Calculator {
    int plus(int sum, int input) {
        return sum + input;
    }

    int minus(int sum, int input) {
        return sum - input;
    }

    int multiple(int sum, int input) {
        return sum * input;
    }

    int divide(int sum, int input) {
        return sum / input;
    }
}
